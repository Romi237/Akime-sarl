let currentLang = localStorage.getItem('akime_lang') || 'fr';

function applyTranslations() {
  console.log('=== applyTranslations() called ===');
  console.log('Current language:', currentLang);
  
  // Wait for DOM to be ready just in case
  if (!document.body) {
    console.log('DOM not ready, waiting...');
    setTimeout(applyTranslations, 100);
    return;
  }

  // Handle data-i18n for text content
  const elements = document.querySelectorAll('[data-i18n]');
  console.log('Number of elements to translate:', elements.length);
  if (elements.length === 0) {
    console.warn('No elements with data-i18n attribute found!');
  }
  
  elements.forEach((el, index) => {
    const key = el.getAttribute('data-i18n');
    console.log(`Element ${index + 1}: key = "${key}"`);
    
    if (translations[currentLang] && translations[currentLang][key]) {
      el.textContent = translations[currentLang][key];
      console.log(`  ✅ Translated "${key}" to: "${translations[currentLang][key]}"`);
    } else {
      console.warn(`  ❌ No translation found for key: "${key}"`);
    }
  });

  // Handle data-i18n-placeholder for input placeholders
  const placeholderElements = document.querySelectorAll('[data-i18n-placeholder]');
  console.log('Number of placeholder elements to translate:', placeholderElements.length);
  
  placeholderElements.forEach((el, index) => {
    const key = el.getAttribute('data-i18n-placeholder');
    console.log(`Placeholder element ${index + 1}: key = "${key}"`);
    
    if (translations[currentLang] && translations[currentLang][key]) {
      el.placeholder = translations[currentLang][key];
      console.log(`  ✅ Translated placeholder "${key}" to: "${translations[currentLang][key]}"`);
    } else {
      console.warn(`  ❌ No translation found for placeholder key: "${key}"`);
    }
  });

  // Update the HTML lang attribute
  document.documentElement.lang = currentLang;
  console.log('=== applyTranslations() completed ===');
}

function switchLang(lang) {
  console.log('=== switchLang() called ===');
  console.log('Switching to language:', lang);
  
  currentLang = lang;
  localStorage.setItem('akime_lang', lang);
  applyTranslations();
  
  console.log('=== switchLang() completed ===');
}

// Expose functions globally
window.switchLang = switchLang;
window.applyTranslations = applyTranslations;
window.translations = translations;
console.log('translations.js loaded!');

// Apply translations when DOM is ready
const initializeTranslations = () => {
  console.log('Initializing translations...');
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => {
      // Wait a little bit to let dynamic content load
      setTimeout(applyTranslations, 500);
    });
  } else {
    applyTranslations();
  }
};

